    private void regbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regbtnActionPerformed
        // TODO add your handling code here:
        
        try
        {
            if(fName.getText().isEmpty() || laName.getText().isEmpty() || phoneNo.getText().isEmpty() || Address.getText().isEmpty() || openBal.getText().isEmpty() || ofId.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Please enter all fields", "whenever", JOptionPane.WARNING_MESSAGE);
            }
            else if(!openBal.getText().matches("\\d+"))
            {
                JOptionPane.showMessageDialog(null, "Balancemust be digits", "Invalid", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                Customers customer = new Customers();
                String selecteditem = (String)genMF.getSelectedItem();
                double newBal = Double.parseDouble(openBal.getText());
                
                boolean register = customer.isRegister(fName.getText(), laName.getText(), selecteditem, Address.getText(), phoneNo.getText(), newBal, ofId.getText());
                
                if(register)
                {
                    JOptionPane.showMessageDialog(null, "Registration was successful!! " + "Your account number is " + customer.getAccountNo(), "Fine", JOptionPane.INFORMATION_MESSAGE);
                    fName.setText("");
                    laName.setText("");
                    Address.setText("");
                    phoneNo.setText("");
                    openBal.setText("");
                    ofId.setText("");
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Something went wrong", "Failed", JOptionPane.ERROR_MESSAGE);
                }
            }
                    
        }
        catch(HeadlessException | NumberFormatException E)
        {
            JOptionPane.showMessageDialog(null, E.getMessage());
        }
        
    }//GEN-LAST:event_regbtnActionPerformed


    private void regbtn3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regbtn3ActionPerformed
        // TODO add your handling code here:
        
        if(TacctNo.getText().isEmpty() || tAmt.getText().isEmpty() || tAmtInWords.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please enter all fields", "whenever", JOptionPane.WARNING_MESSAGE);
        }
        else if(!TacctNo.getText().matches("\\d+") || !tAmt.getText().matches("\\d+"))
        {
            JOptionPane.showMessageDialog(null, "Account Number or Amount must be in digit");
        }
        else if(TacctNo.getText().length() != 10)
        {
            JOptionPane.showMessageDialog(null, "Invalid account number");
        }
        else
        {
            Transactions transac = new Transactions();
            int transAccountNo = Integer.parseInt(TacctNo.getText());
            double transAmt = Double.parseDouble(tAmt.getText());
            String selectedItem = (String)TtransType.getSelectedItem();
            
            boolean isTrans = transac.doTransaction(transAccountNo, selectedItem, transAmt, tAmtInWords.getText());
            
            if(isTrans)
            {
                JOptionPane.showMessageDialog(null, "Transaction was successful!!", "Fine", JOptionPane.INFORMATION_MESSAGE);
                TacctNo.setText("");
                tAmt.setText("");
                tAmtInWords.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Something went wrong", "Fine", JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_regbtn3ActionPerformed

